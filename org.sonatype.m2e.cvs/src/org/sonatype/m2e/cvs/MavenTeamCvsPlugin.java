package org.sonatype.m2e.cvs;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 * 
 * @author Eugene Kuleshov
 */
public class MavenTeamCvsPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "org.maven.ide.eclipse.scm.cvs";

	private static MavenTeamCvsPlugin plugin;
	
	public MavenTeamCvsPlugin() {
	}

	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static MavenTeamCvsPlugin getDefault() {
		return plugin;
	}

}
